/**
 * @param {number[][]} grid
 * @return {number}
 */
var shortestBridge = function(grid) {
    const n = grid.length
    // 寻找第一个1的格子，找到岛屿，然后标记成2
    // const isVisited = new Array(n)
    // for (let i = 0; i < n; i++) {
    //     isVisited[i] = new Array(n).fill(false)
    // }
    const q = []
    const dfs = (i, j) => {
        // isVisited[i][j] = true
        grid[i][j] = 2
        q.push([i, j, 0])
        for (const [x, y] of [[i - 1, j], [i + 1, j], [i, j - 1], [i, j + 1]]) {
            if (x >= 0 && x < n && y >= 0 && y < n && grid[x][y] === 1) {
                dfs(x, y)
            }
        }
    }
    let flag = false
    for (let i = 0; i < n; i++) {
        for (let j = 0; j < n; j++) {
            if (grid[i][j] === 1) {
                flag = true
                dfs(i, j)
            }
            if (flag) break
        }
        if (flag) break
    }

    while (q.length > 0) {
        const item = q[0]
        let i = item[0], j = item[1]
        for (const [x, y] of [[i - 1, j], [i + 1, j], [i, j - 1], [i, j + 1]]) {
            if (x >= 0 && x < n && y >= 0 && y < n) {
                if (grid[x][y] === 1) return item[2]
                if (grid[x][y] === 0) {
                    grid[x][y] = -1     // 代表已经访问过
                    q.push([x, y, item[2] + 1])
                }
            }
        }
        q.shift()
    }
};
